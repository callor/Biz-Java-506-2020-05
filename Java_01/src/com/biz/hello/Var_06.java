package com.biz.hello;

public class Var_06 {
	
	public static void main(String[] args) {
		int num1 = 0;
		int num2 = 0;
		
		num1 = 40;
		num2 = 50;
		
		/*
		 * 변수선언문 또는 선언된 변수 = 값 또는 연산식
		 */
		int sum = num1 + num2;
		sum = 50 - 10;
		sum = 50 / 2;
		sum = 50 * 10;
		
		System.out.println(sum);
	
	}

}

package com.biz.hello;

public class Var_07 {
	
	public static void main(String[] args) {
		
		int num1 = 30;
		int num2 = 40;
		
		System.out.println(num1 + 20); // 30 + 20
		System.out.println(num1 - 10); // 30 - 10
		/*
		 * 변수는 할당, 읽기를 수행하기 전 반드시 선언되어 있어야 한다.
		 * 변수는 읽기를 수행하기 전 반드시 선언 및 할당이 되어 있어야 한다.
		 * (선언만 되어있는 변수는 읽기가 불가능하다)
		 * 이미 할당된 변수에 다른값을 할당하면 이전에 보관된 값을 삭제된다.
		 */
		int num3 = 40;
		
	}

}

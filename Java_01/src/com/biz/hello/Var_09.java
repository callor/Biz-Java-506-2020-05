package com.biz.hello;

public class Var_09 {

	public static void main(String[] args) {
		int num1 = 1;
		System.out.println(3 * num1); // 3
		
		num1 = num1 + 1; // 2
		System.out.println(3 * num1); // 3 * 2

		num1 = num1 + 1; // 3
		System.out.println(3 * num1); // 3 * 3

		num1 = num1 + 1; // 4
		System.out.println(3 * num1); // 3 * 4

		num1 = num1 + 1; // 5
		System.out.println(3 * num1); // 3 * 5

		num1 = num1 + 1; // 6
		System.out.println(3 * num1); // 3 * 6
		
	}
	
	
	
}
